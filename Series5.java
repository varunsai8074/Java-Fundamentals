import java.util.*;
public class Series5
{
    public static void main(String[] args)
    {
        Scanner sc = new Scanner(System.in);
        
        int a=11;
        int n = sc.nextInt();
        
        for(int i=0; i<n;i++)
        {
            double sq = Math.pow(a,2);
            System.out.printf("%.0f ",sq);
            a = a+4;
        }
    }
}
